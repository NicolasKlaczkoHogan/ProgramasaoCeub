valor1 = float(input("valor 1 "))
valor2 = float(input("valor 2 "))
oprtasao = str(input("qual operasao deseja faser "))
if oprtasao == "+":

    resultado = (valor1 + valor2)
    print (resultado)
elif oprtasao == "-":
    resultado = (valor1 - valor2)
    print (resultado)
elif oprtasao == "*":
    resultado = (valor1 * valor2)
    print (resultado)
elif oprtasao == "/":
    resultado = (valor1 / valor2)
    print (resultado)
